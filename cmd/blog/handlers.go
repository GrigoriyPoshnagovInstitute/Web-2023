func featuredPosts(db *sqlx.DB) ([]featuredPostData, error) {
	const query = `
		SELECT
			post_id,
			title,
			subtitle,
			image_modifier,
			publish_date,
			author_url,
			author
		FROM
			post
		WHERE featured = 1
	`
	var upperPosts []featuredPostData

	err := db.Select(&upperPosts, query)
	if err != nil {
		return nil, err
	}

	return upperPosts, nil
}

func mostRecentPosts(db *sqlx.DB) ([]mostRecentPostData, error) {
	const query = `
		SELECT
			post_id,
			title,
			subtitle,
			image_url,
			publish_date,
			author_url,
			author
		FROM
			post
		WHERE featured = 0	
	`
	var posts []mostRecentPostData

	err := db.Select(&posts, query)
	if err != nil {
		return nil, err
	}

	return posts, nil
}

func postByID(db *sqlx.DB, postID int) (postData, error) {
	const query = `
		SELECT
			title,
			subtitle,
			image_url,
			content
		FROM
			post
		WHERE
			post_id = ?
	`
	var post postData

	err := db.Get(&post, query, postID)
	if err != nil {
		return postData{}, err
	}

	return post, nil
}
